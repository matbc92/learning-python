# o comando def permite a definição de uma função custumisada,
# uma vez nomeada a função (na sequencia do comando det), segue-se
# num bloco de codigo indentado as instruções da função, uma vez termianada a função,
# ao se inerir no codigo o nome da função definda, as instruções seram executadas
# sendo assim possivelmente um dos recursos mais importantes do python
def say_hello():
    # block belonging to the function
    print('hello world')
# End of function

say_hello()  # call the function
say_hello()  # call the function again
